// = passo um valor
// == compara dois valores para ver se são iguais
// != compara dois valores para ver se são diferentes
// === compara dois valores para ver se são iguais em valor E tipo
// !== compara dois valores para ver se são diferentes em valor E tipo

let n1=10;

if(n1=="10"){
    console.log("Igual");
}else{
    console.log("Diferente");
}

if(n1==="10"){
    console.log("Igual");
}else{
    console.log("Diferente");
}

if(n1===10){
    console.log("Igual");
}else{
    console.log("Diferente");
}